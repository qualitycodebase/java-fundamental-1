package conditionalStatement;

public class Selection {

	public static void main(String[] args) {
		// If clause , Switch 
		
		String day = "Monday";
		
		
		if(day == "Monday") {
			
			System.out.println("I have to go work !!");		
			
		} else if (day == "Tuesday"){
			
			System.out.println("I go to church!!");
		}else {
			System.out.println("I do not have work !!");
		}

	}

}
